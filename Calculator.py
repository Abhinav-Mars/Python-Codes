def add(n1,n2):
    return n1+n2

def subtract(n1,n2):
    return n1-n2

def multiply(n1,n2):
    return n1*n2

def divide(n1,n2):
    return n1/n2


n1 = int(input("Enter the first number\n"))


operations = {
    "+" : add,
    "-" : subtract,
    "*" : multiply,
    "/" : divide
}



continue_operation = True

while continue_operation:
    operator = input("Enter the required operator\n")
    n2 = int(input("Enter the next number\n"))
    
    for key in operations:
        if key == operator :
            result = operations[key](n1,n2)
            print(f"{n1} {operator} {n2} = {result}")
    
    continue_calculating = (input(f"Enter 'y' if you want to continue the calculations with {result} or else enter 'n'"))
    if continue_calculating == "y":
        n1 = result
    elif continue_calculating == "n":
        continue_operation = False
        print(f"The final output is {result}")
    
        